public class cogni {
    long lat;
    long log;

    public cogni() {
    }

    public cogni(long lat, long log) {

        this.lat = lat;
        this.log = log;
    }
    public long getLat() {
        return lat;
    }

    public void setLat(long lat) {
        this.lat = lat;
    }

    public long getLog() {
        return log;
    }

    public void setLog(long log) {
        this.log = log;
    }
}
